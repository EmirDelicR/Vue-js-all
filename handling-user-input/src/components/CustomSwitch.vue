<template>
  <div>
    <div id="on" @click="switched(true)" :class="{active: !isChecked}">On</div>
    <div id="off" @click="switched(false)" :class="{active: isChecked}">Off</div>
  </div>
</template>

<script>
export default {
  name: 'CustomSwitch',
  props: {
    isChecked: Boolean
  },
  methods: {
    switched (check) {
      this.$emit('input', check);
    }
  }
}
</script>

<style scoped>
